"""Task 7: Tip Distribution (Gender & Smoker Status)
Using a Violin Plot with a split=True parameter, I can compare the tipping density of male vs. female customers while also seeing how smoking habits influence those numbers.
"""

tips = sns.load_dataset("tips")
plt.figure(figsize=(10,8))
sns.violinplot(data = tips , x = 'sex', y = 'tip', hue = 'smoker', split=True, palette= 'magma')
plt.title('tip vs sex')
plt.show()
