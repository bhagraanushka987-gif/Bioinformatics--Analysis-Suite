"""Task 8: Feature Engineering - Tip Percentage
Instead of looking at the raw tip amount, I created a new calculated column: tip_pct (Tip divided by Total Bill). This gives a fairer view of how generous customers are regardless of their bill size.
"""

tips = sns.load_dataset("tips")
plt.figure(figsize=(10,8))
tips['tip_pct'] = (tips['tip'] / tips['total_bill']) * 100
sns.kdeplot(data = tips, x = 'tip_pct', fill=True, color = 'salmon')
plt.title('tip percentage distribution')
plt.xlabel('tip percentage')
sns.despine()
plt.show()